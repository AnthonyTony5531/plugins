  ///   None 0x0 0 无状态
  ///   ScoreChanged 0x1 1 分数变更
  ///   HandicapChanged 0x2 2 让分变更
  ///   OddsGoUp 0x4 4 赔率上升
  ///   OddsGoDown 0x8 8 赔率下降
  ///   Opened 0x10 16 赛事开放
  ///   Paused 0x20 32 赛事暂停
  ///   Closed 0x40 64 赛事关闭
  /// }
  ///
  static String getErrorCode(int s){

