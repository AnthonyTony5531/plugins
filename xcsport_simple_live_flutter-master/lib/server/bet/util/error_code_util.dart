  ///
  ///
  ///
  static String getErrorMsg(int s){
    if(s & 0x40 == 0x40){//赛事关闭
      return '${config.langMap['baseLang']['server']['matchClose']}';
    }
    if(s & 0x20 == 0x20){//赛事暂停
      return '${config.langMap['baseLang']['server']['matchPause']}';
    }
    if(s & 0x0 == 0x0){//无状态
      return '';//''您所选的盘口已关闭';
    }
    if(s & 0x1 == 0x1){
      return '';
    }
    if(s & 0x2 == 0x2){
      return '';
    }
    if(s & 0x4 == 0x4){
      return '';
    }
    if(s & 0x8 == 0x8){
      return '';
    }
    if(s & 0x10 == 0x10){
      return '';
    }
    return '';
  }
}