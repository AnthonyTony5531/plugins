    if(s & 0x40 == 0x40){//赛事关闭
      return '1X010';
    }
    if(s & 0x20 == 0x20){//赛事暂停
      return '1X009';
    }
    if(s & 0x0 == 0x0){//无状态
      return '';//''1X000';
    }
    if(s & 0x1 == 0x1){
      return '';
    }
    if(s & 0x2 == 0x2){
      return '';
    }
    if(s & 0x4 == 0x4){
      return '';
    }
