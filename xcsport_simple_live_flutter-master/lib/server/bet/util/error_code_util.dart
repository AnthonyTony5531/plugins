  /// 例 :
  ///   以回传值 20 为例，以 2 进制表示为 00101000，1 为包含的状态对应 4.10 表后则得 到 Opened,OddsGoUp 此两状 态，若以 20 个别 AND 4 及 16 将获得 4 及 16，表示包含此两个状 态
